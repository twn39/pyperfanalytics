class TestTableCAPM:
    """Verify table_capm against R's table.CAPM output.

    Column positional order: HAM1(col0), HAM2(col1), HAM3(col2).
    R column keys: 'HAM1 to SP500.TR' (dots); Python keys: 'HAM1 to SP500 TR' (space).
    We use positional access (iloc) to avoid name-mangling issues.
    """

    R_COLS = ["HAM1 to SP500.TR", "HAM2 to SP500.TR", "HAM3 to SP500.TR"]  # R keys

    def _r_val(self, bench, col_key, row_name):
        """Get a value from the R benchmark dict by column key and row name."""
        row_idx = bench["table_capm_rownames"].index(row_name)
        return bench["table_capm"][col_key][row_idx]

    def test_alpha_ham1(self, Ra, Rb, tables_bench):
        result = table_capm(Ra, Rb, scale=12, Rf=0)
        r_val = self._r_val(tables_bench, self.R_COLS[0], "Alpha")
        assert result.iloc[result.index.get_loc("Alpha"), 0] == pytest.approx(r_val, abs=TOL)

    def test_beta_ham1(self, Ra, Rb, tables_bench):
        result = table_capm(Ra, Rb, scale=12, Rf=0)
        r_val = self._r_val(tables_bench, self.R_COLS[0], "Beta")
        assert result.iloc[result.index.get_loc("Beta"), 0] == pytest.approx(r_val, abs=TOL)

    def test_correlation_ham1(self, Ra, Rb, tables_bench):
        result = table_capm(Ra, Rb, scale=12, Rf=0)
        r_val = self._r_val(tables_bench, self.R_COLS[0], "Correlation")
        assert result.iloc[result.index.get_loc("Correlation"), 0] == pytest.approx(r_val, abs=TOL)

    def test_tracking_error_ham3(self, Ra, Rb, tables_bench):
        result = table_capm(Ra, Rb, scale=12, Rf=0)
        r_val = self._r_val(tables_bench, self.R_COLS[2], "Tracking Error")
        assert result.iloc[result.index.get_loc("Tracking Error"), 2] == pytest.approx(r_val, abs=TOL)

    def test_information_ratio_ham2(self, Ra, Rb, tables_bench):
        result = table_capm(Ra, Rb, scale=12, Rf=0)
        r_val = self._r_val(tables_bench, self.R_COLS[1], "Information Ratio")
        py_val = result.iloc[result.index.get_loc("Information Ratio"), 1]
        _approx_or_nan(py_val, r_val)

    def test_active_premium_all_cols(self, Ra, Rb, tables_bench):
        result = table_capm(Ra, Rb, scale=12, Rf=0)
        row_idx = result.index.get_loc("Active Premium")
