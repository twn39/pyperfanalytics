TOL = 1e-3


def _approx_or_nan(py_val, r_val, tol=TOL):
    """Assert equal, or both NaN."""
    if r_val is None or (isinstance(r_val, float) and math.isnan(r_val)):
        assert py_val is None or (isinstance(py_val, float) and math.isnan(py_val)) or np.isnan(py_val)
    else:
        assert py_val == pytest.approx(r_val, abs=tol)


