class TestTableAnnualizedReturns:
    """Extended tests for table_annualized_returns."""

    def test_ham1_annualized_return(self, managers):
        ra = managers.iloc[:, 0:6]
        result = table_annualized_returns(ra)
        assert result.loc["Annualized Return", "HAM1"] == pytest.approx(0.137532, abs=1e-4)

    def test_ham1_std_dev(self, managers):
        ra = managers.iloc[:, 0:6]
        result = table_annualized_returns(ra)
        assert result.loc["Annualized Std Dev", "HAM1"] == pytest.approx(0.0887808, abs=1e-4)

    def test_sharpe_with_rf_zero(self, managers):
        ra = managers.iloc[:, 0:6]
        # In arithmetic compounding (geometric=False), the simple division relationship holds exactly
        result = table_annualized_returns(ra, Rf=0, geometric=False)
        # Sharpe = Annualized Return / Annualized Std Dev (when Rf=0 and geometric=False)
        ann_ret = result.loc["Annualized Return", "HAM1"]
        ann_std = result.loc["Annualized Std Dev", "HAM1"]
        sharpe_row = [row for row in result.index if "Sharpe" in row][0]
        expected_sharpe = ann_ret / ann_std
        assert result.loc[sharpe_row, "HAM1"] == pytest.approx(expected_sharpe, abs=TOL)

    def test_output_shape(self, managers):
        ra = managers.iloc[:, 0:6]
        result = table_annualized_returns(ra)
        assert result.shape[0] == 3  # 3 rows: Return, StdDev, Sharpe
        assert result.shape[1] == 6  # 6 assets

    def test_sharpe_label_has_rf(self, managers):
        ra = managers.iloc[:, 0:6]
        result = table_annualized_returns(ra, Rf=0.035 / 12)
        # Sharpe row label should contain "Rf="
        sharpe_rows = [row for row in result.index if "Sharpe" in row]
        assert len(sharpe_rows) == 1
        assert "Rf=" in sharpe_rows[0]
